//Operadores Aritméticos
let n1 = 4
let n2 = 2

//Operador do tipo soma
let soma = n1 + n2
console.log(soma)

//Operador do tipo subtração
let subtracao = n1 - n2
console.log(subtracao)

//Operador do tipo Multiplicacao
let multiplicacao = n1 * n2
console.log(multiplicacao)

//Operador do tipo divisao
let divisao = n1 / n2
console.log(divisao)

//Operador Modulo
let modulo = n1 % n2
console.log(modulo)

//Operador de potencia
let potencia = Math.pow(n1,n2)
console.log(potencia)

//Operador de Raiz Quadrada
let raizQuadrada = Math.sqrt(n1)
console.log(raizQuadrada)
