//Variaveis = var e let
//Declarando VAR
var texto = "Hello Word"
console.log(texto)

//Declarando LET
let nome = "Ana Caroline"
console.log("nome: " , nome)

//Atribuindo um novo valor - LET
nome = "Guilherme"
console.log("Nome: ",nome)

//Atribuindo um novo valor - VAR
texto = "Olá, mundo"
console.log(texto)

//Constantes - const
const valorPi = 3.14
console.log("Ovalor de PI é igual a:",valorPi);
